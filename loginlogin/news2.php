        <p>Suffice it to say, 2020 street style isn’t what it used to be. With everyone living a WFH life, the idea
            of getting dressed up and hitting the town just doesn’t resonate for now. But Rei Shito’s new book,
            Style on the Street: From Tokyo and Beyond, lets us relive the best street style moments of the past
            decade. Published by Rizzoli, Style on the Street is a comprehensive look at Shito’s global archive.</p>

        <p>Shito has chronicled New York, London, Milan, and Paris fashion weeks for 16 years and remains one of the
            best-dressed and most unmissable photographers outside the shows. “I started shooting in 2004 when
            Shoichi Aoki, the editor in chief of FRUiTS, asked me to be a street photographer,” Shito says,
            “suddenly, so it happened.” Since that serendipitous moment, Shito has traveled the world, helping shape
            the look of contemporary street style photography.</p>

        <p>Though the book starts with a self-described “boast”—“I take photos around the globe, and I can’t help but feel that Tokyo is head and shoulders above other cities when it comes to street fashion,” Shito writes—it is a comprehensive look at how the fashionable have dressed for the better part of a decade. “There were many changes over the years. There are times when men’s fashion is more energetic than women’s, and other times on the contrary. There was a movement called Mori-girl in Japan, which was based on the concept of a girl who looks like she is living in the forest, and there were times when normcore was a trend on the other hand,” Shito says. “Sometimes the whole industry is very vigorous and there are times when it’s not. I think the young kids, especially men’s fashion in Tokyo nowadays, is so powerful and fun. They are so passionate about fashion!”</p>
        <p>Sorted by trends, the tome cycles through styles from “Pattern on Pattern” to “Hazushi / Zurashi” (Undoing/Shifting) as a means to encourage readers to try the styles themselves. “Throughout street photography, I strongly felt that fashion provides an opportunity to live your ideal life, a life that affirms who you are, and a power to change your life positively,” she says. “I hope that this book will become a way for readers to enjoy fashion.”</p>
        <p>As for where street style is headed next, Shito feels as unsure as the rest of us. “I don’t know, to be honest, and I think that may be the reason why I do street photography,” she says. “I want to photograph people on the street for as long as I can; it is my lifelong mission.”</p>
